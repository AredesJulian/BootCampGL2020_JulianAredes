
public class Practica_53_Encapsulamiento {

			
		public String getNombre() {
			return nombre;
		}

		public void setNombre(String nombre) {
			this.nombre = nombre;
		}

		public int getPatas() {
			return patas;
		}

		public void setPatas(int patas) {
			this.patas = patas;
		}

		private String nombre;
		private int patas;


}
